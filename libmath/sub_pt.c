#include "libmath.h"

t_pt	sub_pt(t_pt a, t_pt b)
{
	t_pt	ret;

	ret.x = a.x - b.x;
	ret.y = a.y - b.y;
	ret.z = a.z - b.z;
	return (ret);
}
